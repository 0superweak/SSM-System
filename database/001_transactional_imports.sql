create or replace function public.import_students_transactional(payload jsonb)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
    processed integer;
begin
    if not public.is_authorized_operator() then
        raise exception 'SSM operator authorization required' using errcode = '42501';
    end if;
    if jsonb_array_length(payload) = 0 then
        return 0;
    end if;

    -- Serialize imports so two desktop clients cannot create conflicting rosters.
    perform pg_advisory_xact_lock(hashtext('ssm-student-import'));
    lock table public.students in exclusive mode;

    create temporary table import_student_payload on commit drop as
    select
        row_number() over () as import_row,
        last_name,
        first_name,
        gender,
        grade,
        address,
        city,
        area,
        nullif(trim(birthday), '') as birthday,
        sponsor,
        contact,
        school,
        parents,
        course,
        remarks,
        coalesce(nullif(status, ''), 'Active') as status
    from jsonb_to_recordset(payload) as record(
        last_name text,
        first_name text,
        gender text,
        grade text,
        address text,
        city text,
        area text,
        birthday text,
        sponsor text,
        contact text,
        school text,
        parents text,
        course text,
        remarks text,
        status text
    );

    create temporary table import_student_keep on commit drop as
    select p.import_row, matched.id
    from import_student_payload p
    join lateral (
        select s.id
        from public.students s
        where lower(trim(s.last_name)) = lower(trim(p.last_name))
          and lower(trim(s.first_name)) = lower(trim(p.first_name))
        order by (
            nullif(trim(s.birthday), '') is not distinct from p.birthday
        ) desc, s.id
        limit 1
    ) matched on true;

    update public.students s
    set gender = p.gender,
        grade = p.grade,
        address = p.address,
        city = p.city,
        area = p.area,
        birthday = p.birthday,
        sponsor = p.sponsor,
        contact = p.contact,
        school = p.school,
        parents = p.parents,
        course = p.course,
        remarks = p.remarks,
        status = p.status
    from import_student_keep k
    join import_student_payload p on p.import_row = k.import_row
    where s.id = k.id;

    insert into public.students (
        last_name, first_name, gender, grade, address, city, area,
        birthday, sponsor, contact, school, parents, course, remarks, status
    )
    select
        p.last_name, p.first_name, p.gender, p.grade, p.address, p.city, p.area,
        p.birthday, p.sponsor, p.contact, p.school, p.parents, p.course,
        p.remarks, p.status
    from import_student_payload p
    where not exists (
        select 1
        from import_student_keep k
        where k.import_row = p.import_row
    );

    select count(*) into processed from import_student_payload;
    return processed;
end;
$$;

