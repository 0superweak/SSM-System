create or replace function public.replace_coordinators_transactional(payload jsonb)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
    inserted_count integer;
begin
    if not public.is_authorized_operator() then
        raise exception 'SSM operator authorization required' using errcode = '42501';
    end if;
    lock table public.coordinators in exclusive mode;
    delete from public.coordinators where true;
    insert into public.coordinators (
        location, contact_person, email, contact_no, fb_page, remarks
    )
    select location, contact_person, email, contact_no, fb_page, remarks
    from jsonb_to_recordset(payload) as record(
        location text,
        contact_person text,
        email text,
        contact_no text,
        fb_page text,
        remarks text
    );
    get diagnostics inserted_count = row_count;
    return inserted_count;
end;
$$;

