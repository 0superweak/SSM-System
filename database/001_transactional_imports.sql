-- Run once in the Supabase SQL editor before deploying the desktop clients.
-- These functions keep destructive replacements and duplicate-safe imports in
-- a single database transaction. This deployment uses a shared office client:
-- one of three operator names is selected in the desktop app for audit records,
-- while the Supabase publishable key supplies the database role.

create table if not exists public.app_authorized_users (
    user_id uuid primary key references auth.users(id) on delete cascade,
    display_name text not null,
    active boolean not null default true,
    created_at timestamptz not null default now()
);

alter table public.app_authorized_users enable row level security;
revoke all on public.app_authorized_users from anon, authenticated;

create or replace function public.is_authorized_operator()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select auth.role() in ('anon', 'authenticated', 'service_role');
$$;

create or replace function public.current_operator_name()
returns text
language sql
stable
security definer
set search_path = public
as $$
    select display_name
    from public.app_authorized_users
    where user_id = auth.uid()
      and active
    limit 1;
$$;

revoke all on function public.is_authorized_operator() from public;
revoke all on function public.current_operator_name() from public, anon;
grant execute on function public.is_authorized_operator() to anon, authenticated;
grant execute on function public.current_operator_name() to authenticated;

