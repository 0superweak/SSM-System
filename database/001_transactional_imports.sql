-- Supabase Storage policies for student photos. The bucket itself must already
-- exist and remain private.
drop policy if exists "authorized operators read student photos" on storage.objects;
create policy "authorized operators read student photos"
    on storage.objects for select to anon, authenticated
    using (
        bucket_id = 'student-photos'
        and public.is_authorized_operator()
    );

drop policy if exists "authorized operators add student photos" on storage.objects;
create policy "authorized operators add student photos"
    on storage.objects for insert to anon, authenticated
    with check (
        bucket_id = 'student-photos'
        and public.is_authorized_operator()
    );

drop policy if exists "authorized operators update student photos" on storage.objects;
create policy "authorized operators update student photos"
    on storage.objects for update to anon, authenticated
    using (
        bucket_id = 'student-photos'
        and public.is_authorized_operator()
    )
    with check (
        bucket_id = 'student-photos'
        and public.is_authorized_operator()
    );

drop policy if exists "authorized operators delete student photos" on storage.objects;
create policy "authorized operators delete student photos"
    on storage.objects for delete to anon, authenticated
    using (
        bucket_id = 'student-photos'
        and public.is_authorized_operator()
    );
