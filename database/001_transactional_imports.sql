do $$
begin
    -- Legacy installs may still have name columns marked NOT NULL. The desktop
    -- importer now writes canonical student_id links, so those text columns must
    -- be optional if they still exist.
    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
          and table_name = 'donor_students'
          and column_name = 'last_name'
    ) then
        alter table public.donor_students alter column last_name drop not null;
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
          and table_name = 'donor_students'
          and column_name = 'first_name'
    ) then
        alter table public.donor_students alter column first_name drop not null;
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
          and table_name = 'student_movements'
          and column_name = 'last_name'
    ) then
        alter table public.student_movements alter column last_name drop not null;
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
          and table_name = 'student_movements'
          and column_name = 'first_name'
    ) then
        alter table public.student_movements alter column first_name drop not null;
    end if;
end;
$$;

